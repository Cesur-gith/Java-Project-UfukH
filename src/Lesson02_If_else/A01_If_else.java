package Lesson02_If_else;

public class A01_If_else {

    public static void main(String[] args) {


        int sayi = -5;

        if (sayi < 0) {
            System.out.println(sayi + "Negatif");

        } else if (sayi > 0) {
            System.out.println(sayi + "Pozitif");
        } else {
            System.out.println(sayi + "Notr");
        }


    }

}
